
#-----------------------------------------------------------------------------------------
#Cargar librerias
#----------------------------------------------------------------------------------------

library(forecast)


#--------------------------------------------------------------------

tiempo=c(1:(12*10))

x=rnorm(1,0,1)


t=1

while(t<=120){
  
  
  x[t]=rnorm(1,0,1)
  
  
  t=t+1  
}

#-----------------------------------------------------------------------


plot(x, ylab="Xt", xlab="Tiempo" ,type="o",lwd=1,pch=1,cex=1,col="Green")
abline(h=0)



#Sumar estacionalidad


i=1

while(i<=10){
  
  
  x[12*i]=5
  
  
  i=i+1  
}


#-----------------------------------------------------------------------


plot(x, ylab="Xt", xlab="Tiempo" ,type="o",lwd=1,pch=1,cex=1,col="Green")
abline(h=0)



#------------------------------------------------------------------------
#Crear datos ts
x = ts(x,frequency = 12)



#------------------------------------------------------------------------



fit=auto.arima(x,seasonal = T,ic = c("aicc"), stepwise=T, allowdrift = F)
summary(fit)


