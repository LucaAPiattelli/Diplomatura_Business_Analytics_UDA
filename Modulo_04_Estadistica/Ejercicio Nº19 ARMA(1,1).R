library(forecast)


#--------------------------------------------------------------------

tiempo=c(1:100)

x=rnorm(1,0,1)
e=rnorm(1,0,1)

t=2

while(t<=100){
  
  e[t]=rnorm(1,0,1) 
  x[t]=0.6*x[t-1]+0.6*e[t-1]+e[t]
  
  
  t=t+1  
}


#-----------------------------------------------------------------------


plot(x, ylab="Xt", xlab="Tiempo" ,type="o",lwd=1,pch=1,cex=1,col="Green")
abline(h=0)


#-------------------------------------------------------------------

fit=auto.arima(x,seasonal = F,ic = c("aicc"), stepwise=T, allowdrift = F)
summary(fit)

